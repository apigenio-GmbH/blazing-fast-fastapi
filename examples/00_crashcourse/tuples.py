tuple1 = ()
print("Initial empty Tuple: ")
print(tuple1)

tuple2 = ('Heroes', 'For')
print("\nTuple with the use of String: ")
print(tuple2)

list1 = [1, 2, 4, 5, 6]
print("\nTuple using List: ")
print(tuple(list1))

tuple3 = tuple('Heroes')
print("\nTuple with the use of function: ")
print(tuple3)

tuple4 = (0, 1, 2, 3)
tuple5 = ('python', 'hero')
tuple6 = (tuple4, tuple5)
print("\nTuple with nested tuples: ")
print(tuple6)